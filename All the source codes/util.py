def get_padded_text(text, length, padding=' '):
    padded_char_arr = [padding for i in range(length)]
    
    for index in range(min(length, len(text))):
        padded_char_arr[index] = text[index]
        
    return "".join(padded_char_arr)

def get_numeric_value(string):
    value = 0
    
    for ch in string:
        value = (value << 8) + ord(ch)
        
    return value

def string_to_hex(string):        
    return hex(get_numeric_value(string))[2:]

def get_byte_array(string):
    num_val = get_numeric_value(string)
    arr = []
    
    while num_val > 0:
        arr.append(num_val & 255)
        num_val >>= 8

    arr.reverse()
    return arr

def get_words(string):
    byte_array = get_byte_array(string)
    return [byte_array[i*4:(i*4)+4] for i in range(4)]

def substitute_bytes(sbox, word):
    return [sbox[(byte >> 4) & 15][byte & 15] for byte in word]

def substitute_bytes_in_matrix(sbox, matrix):
    return [[sbox[(byte >> 4) & 15][byte & 15] for byte in word] for word in matrix]

def get_round_constant(round_no):
    key = 1

    if round_no <= 4:
        key = 0x01 << (round_no - 1)
    elif round_no <= 8:
        key = 0x10 << (round_no - 5)
    elif round_no <= 10:
        key = 0x1b << (round_no - 9)
        
    return [key, 0x00, 0x00, 0x00]

def word_xor(word_1, word_2):
    word_res = []
    
    for i in range(len(word_1)):
        word_res.append(word_1[i] ^ word_2[i])
        
    return word_res

def g(round_no, sbox, word):
    temp_word = copy.deepcopy(word)
    for i in range(len(word) - 1):
        temp_word[i], temp_word[i+1] = temp_word[i+1], temp_word[i]
        
    sub_bytes = substitute_bytes(sbox, temp_word)
    return word_xor(get_round_constant(round_no), sub_bytes)

def get_round_keys(sbox, key):
    words = get_words(key)
    i = len(words)
    
    while i < 44:
        words.append(word_xor(words[i-4], g(i >> 2, sbox, words[i-1]) if i % 4 == 0 else words[i-1]))
        i += 1
        
    return [list(itertools.chain.from_iterable(words[i:i+4])) for i in range(0, len(words), 4)]

def transpose(sq_matrix):
    res_matrix = copy.deepcopy(sq_matrix)

    for i in range(len(sq_matrix)):
        for j in range(len(sq_matrix)):
            res_matrix[i][j] = sq_matrix[j][i]
            
    return res_matrix

def split_list(lst, chunk_size):
    return [lst[i:i + chunk_size] for i in range(0, len(lst), chunk_size)]

def get_round_key_matrix(round_key):
    return transpose(split_list(round_key, int(math.sqrt(len(round_key)))))

def cyclic_shift(arr, offset):
    return [arr[(index - offset) % len(arr)] for index in range(len(arr))]

def galois_mul_byte(b1, b2):
    product = 0
    
    a = b1
    b = b2
    
    for _ in range(8):
        if (b & 0x1) == 1:
            product ^= a
            
        a8set = (a & (1 << 7) != 0)
        a = (a << 1) & 0xff
        
        if a8set:
            a ^= 0x1b
        
        b = (b >> 1) & 0xff
    
    return product